package sectionTen;

public class abstractNotes {
    // Abstract Classes
    // 1. Signifies abstractness
    // 2. Non-instantiable, but defines common protocol for subclasses
    // 3. Abstract classe should be extended
    // 4. Subclass objects do the real work
    /*
     * E.g abstract class Bookmark { ... }
     */

    // Abstract Method
    // Has no body, starts with abstract
    // MUST be overriden, cannot be static

    // Abstract methods MUST be in an abstract class
    // Abstract classes can have both ABSTRACT and CONCRETE methods

    // Abstract Subclass
    // 1. need not override abstract methods
    // 2. Can still override them
    // 3. Can define abstract & concrete methods

    // Concrete Subclass
    // 1. Must override unimplementated abstract methods

}
