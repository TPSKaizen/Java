package SectionNine;

public class inheritanceExample {
    // Subclasses are specialied versions of superClasses

    // They can:
    // 1. Inherit members
    // 2. Add new members
    // 3. Override superclass members

    // Super Class cannot use subclass methods

    // A class can only extend from one class

    // Inheritance Accessibility :
    // Private ~ not inherited
    // Default ~ Inherited IF from family (Same package)
    // ~ Only family can access inherited members
    // Public ~ Anyone can access inherited members
    // Protected ~ Allow access to my family members, even outside the package
}
