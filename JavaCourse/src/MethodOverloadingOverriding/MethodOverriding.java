package MethodOverloadingOverriding;

public class MethodOverriding {
	/*
	 * Overriding is use with inheritance
	 * IF a super class defines a method and then 
	 * the subclass tries to define a method with the SAME
	 * signature/name, this is called overriding
	 */
	
	// RULES
	
	/*								SuperClass Instance		Super Class Static method
	 * 										Method
	 * 		
	 * 	Subclass Instance					Overrides			Generates compile time error
	 * 
	 *  Subclass Static Instance			Generates
	 *  								Compile-time error		Hides
	 * 
	 * Static methods belong to the class, not the object
	 * Instance methods belong to the objects
	 */
}
