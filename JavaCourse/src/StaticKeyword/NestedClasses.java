package StaticKeyword;

public class NestedClasses {
	/*
	 * There are static nested classes and inner classes
	 * 
	 * Why use nested classes?
	 * It is a logical way of grouping classes that are only used in one place
	 * 
	 * It increases encapsulation
	 * e.g
	 * Imagine two top level classes A and B where B needs
	 * to access members of A that otherwise would have
	 * been declared private. By hiding B inside A , A's 
	 * members can be declared private and B can access them.
	 * Additionally B will be hidden rom the world
	 * 
	 * It can lead to more readable and maintainable code
	 */
	
}
